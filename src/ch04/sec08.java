package ch04;

public class sec08 {

	public static void main(String[] args) {
		// print from 1 to 10 example
		int			i	=	1;
		while	(i<=10) {
			System.out.println(i + " ");
			i++;
		} // while end
		int			j	=	1;
		int			sum = 0;
		while (j<=100)	{
			sum		+= j;
			j++;
		} //while end
		System.out.println(sum);
	} // main end

} // class sec08 end
